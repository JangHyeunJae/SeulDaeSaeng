public class HwSubmitVO {
	
	 private int submitNo;
	 private String submitDt;
	 private int fileNo;
	 private int hwNo;
	 private int usersNo;
	 
	public int getSubmitNo() {
		return submitNo;
	}
	public void setSubmitNo(int submitNo) {
		this.submitNo = submitNo;
	}
	public String getSubmitDt() {
		return submitDt;
	}
	public void setSubmitDt(String submitDt) {
		this.submitDt = submitDt;
	}
	public int getFileNo() {
		return fileNo;
	}
	public void setFileNo(int fileNo) {
		this.fileNo = fileNo;
	}
	public int getHwNo() {
		return hwNo;
	}
	public void setHwNo(int hwNo) {
		this.hwNo = hwNo;
	}
	public int getUsersNo() {
		return usersNo;
	}
	public void setUsersNo(int usersNo) {
		this.usersNo = usersNo;
	}
	 
	 
}
